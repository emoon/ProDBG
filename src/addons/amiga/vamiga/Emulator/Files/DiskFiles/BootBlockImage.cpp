BootBlockImage::BootBlockImage(const u8 *buffer)
{
    memcpy(data, buffer, 1024);
    isize i,j;
    
    // Try to find a match in the data base
    for (i = 0; i < isizeof(bbRecord) / isizeof(BBRecord); i++) {
        
        if (bbRecord[i].type == BB_STANDARD && bbRecord[i].image) {
            
            // For standard boot blocks, we require a perfect match
            if (memcmp(buffer, bbRecord[i].image, bbRecord[i].size) == 0) {
                
                type = bbRecord[i].type;
                name = bbRecord[i].name;
                return;
            }
            
        } else {
            
            // For all other blocks, we check specific byte locations
            for (j = 0; j < 7; j++) {
                
                u16 pos = bbRecord[i].signature[2*j];
                u16 val = bbRecord[i].signature[2*j + 1];
                if (pos && data[pos] != val) break;
            }
            
            if (j == 7) {
                
                type = bbRecord[i].type;
                name = bbRecord[i].name;
                return;
            }
        }
    }
}

BootBlockImage::BootBlockImage(const char *name)
{
    memset(data, 0, 1024);
    
    for (isize i = 0; i < isizeof(bbRecord) / isizeof(BBRecord); i++) {
        
        if (strcmp(bbRecord[i].name, name) == 0) {
            memcpy(this->data, bbRecord[i].image, bbRecord[i].size);
            this->type = bbRecord[i].type;
            this->name = bbRecord[i].name;
            return;
        }
    }
}

BootBlockImage::BootBlockImage(BootBlockId bootBlockID)
{
    memset(data, 0, 1024);
    const char *name;
    
    switch (bootBlockID) {
            
        case BB_AMIGADOS_13: name = "AmigaDOS Standard Bootblock 1.3"; break;
        case BB_AMIGADOS_20: name = "AmigaDOS Standard Bootblock 2.0"; break;
        case BB_SCA:         name = "SCA Virus";                       break;
        case BB_BYTE_BANDIT: name = "Byte Bandit 1 Virus";             break;
        default:             name = "";                                break;
    }
    
    BootBlockImage bb = BootBlockImage(name);
    *this = bb;
}

void
BootBlockImage::write(u8 *buffer, isize first, isize last)
{
    assert(first < last && last < 1024);
    
    for (isize i = 0; i < last - first; i++) {
        buffer[i] = data[first + i];
    }
}
